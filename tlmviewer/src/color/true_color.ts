function gammaCorrectSrgb(c: number): number {
    const threshold = 0.0031308;
    const a = 0.055;
    const gamma = 1 / 2.4;

    if (c <= threshold) {
        return 12.92 * c;
    } else {
        return (1 + a) * Math.pow(c, gamma) - a;
    }
}

function interp1d(X: number[], Y: number[], newX: number): number {
    let left = 0;
    let right = X.length - 1;

    // Find the two closest points for interpolation
    while (left < right) {
        const mid = Math.floor((left + right) / 2);
        if (X[mid] < newX) {
            left = mid + 1;
        } else {
            right = mid;
        }
    }

    if (left > 0) left--;

    // Linear interpolation
    const xL = X[left];
    const xR = X[right];
    const yL = Y[left];
    const yR = Y[right];

    return yL + ((yR - yL) * (newX - xL)) / (xR - xL);
}

export function wavelengthToRgb(v: number[]): number[][] {
    const LEN_MIN = 380;
    // const LEN_MAX = 780;
    const LEN_STEP = 5;

    const N = v.length;
    const rgb: number[][] = new Array(N).fill(0).map(() => [0, 0, 0]);

    const visibleSpace = Array.from(
        { length: CIE_X.length },
        (_, i) => LEN_MIN + i * LEN_STEP,
    );

    for (let i = 0; i < N; i++) {
        const X = interp1d(visibleSpace, CIE_X, v[i]);
        const Y = interp1d(visibleSpace, CIE_Y, v[i]);
        const Z = interp1d(visibleSpace, CIE_Z, v[i]);

        const RGB = [
            MATRIX_SRGB_D65[0][0] * X +
                MATRIX_SRGB_D65[0][1] * Y +
                MATRIX_SRGB_D65[0][2] * Z,
            MATRIX_SRGB_D65[1][0] * X +
                MATRIX_SRGB_D65[1][1] * Y +
                MATRIX_SRGB_D65[1][2] * Z,
            MATRIX_SRGB_D65[2][0] * X +
                MATRIX_SRGB_D65[2][1] * Y +
                MATRIX_SRGB_D65[2][2] * Z,
        ];

        rgb[i] = RGB.map((c) => {
            const corrected = gammaCorrectSrgb(c);
            return Math.max(0, Math.min(1, corrected));
        });
    }

    return rgb;
}

export function arrayToRgb(colorArray: number[]): string {
    const r = Math.floor(256 * colorArray[0]);
    const g = Math.floor(256 * colorArray[1]);
    const b = Math.floor(256 * colorArray[2]);

    return `rgb(${r}, ${g}, ${b})`;
}
